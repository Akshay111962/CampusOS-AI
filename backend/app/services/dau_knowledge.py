def _tokenize(text: str) -> list[str]:
    """Lowercase, split on non-word chars, remove stop words and short tokens."""
    import re
    tokens = re.findall(r'\w+', text.lower())
    return [t for t in tokens if len(t) > 2 and t not in _STOPWORDS]


def score_chunk(query: str, chunk: dict) -> float:
    """
    Score a knowledge chunk against a query using TF-IDF-style keyword overlap.
    
    - Title/section matches are weighted 5×
    - Body text matches are weighted 1×
    - Returns a float score (higher = more relevant)
    """
    query_tokens = set(_tokenize(query))
    if not query_tokens:
        return 0.0

    section_tokens = set(_tokenize(chunk.get("section", "")))
    body_tokens = _tokenize(chunk.get("text", ""))
    body_token_set = set(body_tokens)

    score = 0.0
    for token in query_tokens:
        if token in section_tokens:
            score += 5.0  # section header match is a strong signal
        if token in body_token_set:
            # Also count frequency in body for extra weight
            freq = body_tokens.count(token)
            score += 1.0 + (0.2 * min(freq - 1, 4))  # up to +0.8 for frequent terms

    return score


def retrieve_relevant_chunks(query: str, top_k: int = 4) -> list[dict]:
    """
    Retrieve the top-k most relevant knowledge base chunks for a given query.
    Returns list of dicts with 'section' and 'text' keys.
    """
    chunks = get_knowledge_chunks()
    scored = [(score_chunk(query, c), c) for c in chunks]
    scored.sort(key=lambda x: x[0], reverse=True)
    # Return top_k chunks that have at least some relevance
    return [c for score, c in scored[:top_k] if score > 0] or [c for _, c in scored[:2]]
