# Chunk the knowledge base into smaller paragraphs for retrieval
def get_knowledge_chunks() -> list[dict]:
    """Split the knowledge base into retrievable chunks with section labels."""
    chunks = []
    current_section = "General"
    current_text = []
    
    for line in DAU_KNOWLEDGE_BASE.split('\n'):
        if line.startswith('## '):
            if current_text:
                chunks.append({
                    "section": current_section,
                    "text": '\n'.join(current_text).strip()
                })
            current_section = line[3:].strip()
            current_text = []
        elif line.startswith('### '):
            # sub-sections can be part of parent
            current_text.append(line)
        else:
            current_text.append(line)
    
    if current_text:
        chunks.append({
            "section": current_section,
            "text": '\n'.join(current_text).strip()
        })
    
    return [c for c in chunks if c["text"].strip()]


